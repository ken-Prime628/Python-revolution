#Arithmetic Operators - Simple Calculations
x =20
y =6

print(x+y)
print(x-y)
print(x*y)
print(x/y)
print(x%y)  #Modulus- Returns Remainder After Division


#Comparison Operators - Comparing Values
print(x<y)
print(x>y)
print(x<=y)
print(x>=y)
print(x==y) #Equal to
print(x!=y) #Not Equal to


#Assignment Operators - Used to Assign/Store Values in variables
number =45
print(number)

number += 2
print(number)


#Logical Operators - and, or, not
num1 =67
num2 =43
print(num1==num2 and num1>num2) #return a true is all the conditions are true
print(num1==num2 or num1>num2) #return a true is any the conditions are true
print(not(num1==num2 or num1>num2)) #reverses a result if its true it returns a false


